/* ── Daily movie selection ── */

export function getDailyMovie(): Movie {
  const today = new Date()
  const dayIndex =
    today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate()
  const index = dayIndex % MOVIE_LIBRARY.length
  return MOVIE_LIBRARY[index]
}

export function getMovieById(id: string): Movie | undefined {
  return MOVIE_LIBRARY.find((m) => m.id === id)
}

export function searchMovies(query: string): Movie[] {
  const q = query.toLowerCase()
  return MOVIE_LIBRARY.filter(
    (m) =>
      m.title.toLowerCase().includes(q) ||
      m.titleEn.toLowerCase().includes(q) ||
      m.genres.some((g) => g.toLowerCase().includes(q)) ||
      m.tags.some((t) => t.toLowerCase().includes(q)) ||
      m.director.toLowerCase().includes(q)
  )
}

export function getMoviesByGenre(genre: string): Movie[] {
  return MOVIE_LIBRARY.filter((m) =>
    m.genres.some((g) => g.toLowerCase() === genre.toLowerCase())
  )
}

export function getMoviesByTag(tag: string): Movie[] {
  return MOVIE_LIBRARY.filter((m) =>
    m.tags.some((t) => t.toLowerCase() === tag.toLowerCase())
  )
}

export function getRandomMovies(count: number): Movie[] {
  const shuffled = [...MOVIE_LIBRARY].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, count)
}

export function getMoviesByMood(mood: string): Movie[] {
  const moodMap: Record<string, string[]> = {
    'happy': ['喜剧', '动画', '爱情', '治愈', '温情', '励志', '梦想'],
    'sad': ['催泪', '治愈', '亲情', '文艺', '分手', '遗憾'],
    'excited': ['动作', '科幻', '冒险', '悬疑', '犯罪', '惊悚'],
    'relaxed': ['治愈', '日常', '文艺', '爱情', '喜剧', '动画'],
    'thoughtful': ['哲学', '悬疑', '烧脑', '心理', '社会', '人性'],
    'romantic': ['爱情', '浪漫', '纯爱', '初恋', '前任', '文艺'],
    'nostalgic': ['经典', '怀旧', '青春', '成长', '家庭', '温情'],
    'adventurous': ['冒险', '科幻', '动作', '太空', '公路', '史诗'],
    'melancholy': ['文艺', '治愈', '孤独', '遗憾', '分手', '人生'],
  }

  const genres = moodMap[mood.toLowerCase()] || []
  if (genres.length === 0) return getRandomMovies(5)

  return MOVIE_LIBRARY.filter((m) =>
    m.genres.some((g) => genres.includes(g.toLowerCase())) ||
    m.tags.some((t) => genres.includes(t.toLowerCase()))
  )
}
